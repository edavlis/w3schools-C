#include <math.h>
#include <stdio.h>

int main() {
	double w = 16;
	float x = 4.44;
	float y = 5.55;
	int z = 2;

	// square root number
	printf("\n%f\n", sqrt(w)); // square root
	printf("\n%f\n", ceil(x)); // round down to nearest integer
	printf("\n%f\n", floor(y)); // round up to nearest integer
	printf("\n%f\n", pow(z, 4)); // z, to the power of 4
}
